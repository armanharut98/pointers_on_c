int identity_matrix( int *p, int size )
{
    int i = 0;
    while (i < size) {
        int j = 0;
        while (j < size) {
            if (i == j && *p != 1) return FALSE;
            if (i != j && *p == 1) return FALSE;
            j++;
            p++;
        }
        i++;
    }
    return TRUE;
}